    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
 
            DadosMovimentacoes dm = new DadosMovimentacoes();

            dm.setNomeMotorista(txtMotorista.getText());
            dm.setModelo(txtModelo.getText());
            dm.setMarca(txtMarca.getText());
            dm.setQuilometragemInicial(Double.parseDouble(txtQuiloInicial.getText()));
            dm.setQuilometragemFinal(Double.parseDouble(txtQuiloFinal.getText()));

            try {
            dm.setData(dat.parse(fieldData.getText()));
        } catch (ParseException ex) {
            Logger.getLogger(CadastroMovimentacoes.class.getName()).log(Level.SEVERE, null, ex);
        }
            
            MetodosAcoes x = new MetodosAcoes();
            x.salvar(dm);

            Limpar();

    }//GEN-LAST:event_btnCadastrarActionPerformed

